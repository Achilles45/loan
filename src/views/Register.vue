<script>
import Navbar from '@/components/Navbar.vue';
import Footer from '@/components/Footer.vue';

import { setTimeout } from 'timers';
import db from '@/firebase/init';
import firebase from 'firebase';
export default {
    name: 'Register',
    components:{
        Navbar,
        Footer
    },
    data(){
        return{
            fname: null,
            lname: null,
            email: null,
            phone: null,
            country:null,
            gender:null,
            password: null,
            repeatPassword:null,
            err: null,
            loader:false,
            success:null
        }
    },
    methods:{
        clearAlert:function(){
            setTimeout(()=>{
                document.querySelector('.errors').remove();
            }, 5000)
        },
        clearLoader:function(){
            setTimeout(()=>{
                document
            })
        },
        register(){
           //Check if the form fields were left empty
           if(!this.fname || !this.lname || !this.email || !this.phone || !this.country || !this.gender || !this.password || !this.repeatPassword){
               this.err = 'Registration failed. Please fill in all the fields'
               this.clearAlert()
           }else{
               firebase.auth().createUserWithEmailAndPassword(this.email, this.password)
               .then(cred=>{
                   db.collection('users').add({
                       email:this.email,
                       phone:this.phone,
                       first__name:this.fname,
                       last__name:this.lname,
                       country:this.country,
                       gender:this.gender,
                       balance:'00.00',
                       loan__collected: '00.00',
                       unpaid__loan: '00.00',
                       user__id: cred.user.uid
                   })
               })
                .then(()=>{
                    this.$router.push({name: 'Dashboard'})
                })
                .catch(err =>{
                    this.err = err.message;
                    console.log(err.message)
                })
            }
        }
    }
}
